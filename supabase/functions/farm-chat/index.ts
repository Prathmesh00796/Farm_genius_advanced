import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");

    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing chat with", messages?.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          {
            role: "system",
            content: `You are FarmGenius AI, an expert agricultural assistant helping Indian farmers with:

1. **Crop Management**: Planting schedules, crop rotation, intercropping
2. **Pest & Disease Control**: Identification, organic/chemical treatments
3. **Soil Health**: Testing, amendments, fertilizer recommendations
4. **Irrigation**: Water management, drip/sprinkler systems
5. **Market Information**: Price trends, best selling times
6. **Government Schemes**: PM-KISAN, crop insurance, subsidies
7. **Weather Advice**: Seasonal planning, climate adaptation
8. **Modern Techniques**: Precision farming, drone usage, IoT sensors

Guidelines:
- Respond in simple, farmer-friendly language
- Use Hindi terms when helpful (with English translations)
- Provide specific, actionable advice
- Mention local varieties and practices relevant to India
- Include cost estimates in INR when discussing inputs
- Suggest both organic and conventional options
- Be encouraging and supportive

Always greet farmers warmly and ask clarifying questions if needed.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      throw new Error(`AI gateway error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });

  } catch (error) {
    console.error("Error in farm-chat function:", error);
    const errorMessage = error instanceof Error ? error.message : "Unknown error";
    return new Response(JSON.stringify({ error: errorMessage }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
